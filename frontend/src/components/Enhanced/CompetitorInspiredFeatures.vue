import { ref, reactive, onMounted } from 'vue'
import {
  Microphone, ChatDotRound, TrendCharts, View, Lock, UserFilled, School, Grid, Briefcase, Trophy, Connection, Cpu, Tools
} from '@element-plus/icons-vue'

export default {
  name: 'CompetitorInspiredFeatures',
  components: {
    Microphone, ChatDotRound, TrendCharts, View, Monitor, Lock,
    UserFilled, School, DataAnalysis, Briefcase, Trophy, Connection,
    Cpu, Tools
  },
  setup() {
    const activeScenario = ref('campus')
    
    const scenarios = [
      { id: 'campus', name: 'æ ¡å›­æ‹›è˜', icon: 'School' },
      { id: 'social', name: 'ç¤¾ä¼šæ‹›è˜', icon: 'Briefcase' },
      { id: 'technical', name: 'æŠ€æœ¯æ‹›è˜', icon: 'Cpu' }
    ]

    const animatedStats = reactive({
      interviews: 0,
      accuracy: 0,
      companies: 0,
      satisfaction: 0
    })

    const targetStats = {
      interviews: 500,
      accuracy: 98,
      companies: 1000,
      satisfaction: 95
    }

    const switchScenario = (scenarioId) => {
      activeScenario.value = scenarioId
    }

    const animateNumbers = () => {
      const duration = 2000
      const steps = 60
      const stepDuration = duration / steps

      Object.keys(targetStats).forEach(key => {
        const target = targetStats[key]
        const step = target / steps
        let current = 0

        const timer = setInterval(() => {
          current += step
          if (current >= target) {
            animatedStats[key] = target
            clearInterval(timer)
          } else {
            animatedStats[key] = Math.floor(current)
          }
        }, stepDuration)
      })
    }

    onMounted(() => {
      setTimeout(animateNumbers, 500)
    })

    return {
      activeScenario,
      scenarios,
      animatedStats,
      switchScenario
    }
  }
}
</script>

